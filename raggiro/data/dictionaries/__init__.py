"""Dictionary files for spelling correction."""

# This directory contains spelling dictionary files for various languages.
# Dictionary files should be named as frequency_dictionary_LANGCODE.txt
# Where LANGCODE is a two-letter language code like 'en', 'it', 'fr', etc.
#
# Files should contain word frequency data in the format:
# word\tfrequency
#
# Example:
# the    23135851162
# of     13151942776
# and    12997637966
# to     12136980858